var b10tob62 = b10 => {
  var chars = "0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ"
  var b62 = ""
  while (b10 > 0) {
    var mod = b10 % 62
    b62 = chars.charAt(mod) + b62
    b10 = (b10 - mod) / 62
  }
  return b62 || "0"
}
